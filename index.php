  <h1>カスタマイズ機能付き文書作成システム</h1>

  <a href="teams.php">企業一覧</a>

  <h2>機能</h2>

  <ul>
    <li>企業毎に自社のフォーマットを作成できる．</li>
    <li>作成したフォーマットで文書を作成できる．</li>
    <li>作成した文書の詳細を確認することができる．</li>
  </ul>

  <h2>使い方</h2>
  <ol>
    <li>企業を選ぶ（データ作成済み）．</li>
    <li>文書のフォーマットを選択する（ない場合は新規に作成する）．</li>
    <li>フォーマットに従って項目を入力する．</li>
    <li>文書を作成すると，フォーマット毎に文章画面へのリンクが表示される．</li>
    <li>リンクをクリックすると各文書の詳細が確認できる．</li>
  </ol>

